 /**
  *This program calculates circumference of a circle.
  *
  *@author Azime Ulker
  *@version 1.0
  */
public class Circumference
 {
     public static void main(String[] args)
     {
     	double radius = 3.2;

     	final double PI = 3.14;

     	double calculation = 2 * PI * radius;

		System.out.println("Circumference of a circle is " + calculation + ".");
     }
 }