/**
*This program takes a number as input and prints
*its multiplication table upto 10.
*
*@author Azime Ulker
*@version 1.0
*/

public class Multiplication
{
    public static void main(String[] args)
    {
    	int a = 8;

    	for(int i = 1; i <= 10; i++)
    	{
			System.out.println(a +" * " + i + " = " + a * i );
		}
    }

}