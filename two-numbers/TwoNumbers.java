/**
*This program prints the sum, multiply, subtract, divide,
* and remainder of two numbers.
*
*@author Azime Ulker
*@version 1.0
*/

public class TwoNumbers
{
    public static void main(String[] args)
    {
    	int firstNum = 125;
    	int secondNum = 24;

    	System.out.println(firstNum + secondNum);
    	System.out.println(firstNum - secondNum);
    	System.out.println(firstNum * secondNum);
    	System.out.println(firstNum / secondNum);
    	System.out.println(firstNum % secondNum);
    }
}